
/// <summary>
/// ゲーム内で使う操作の種類のタイプ
/// </summary>
public enum GameActionType
{
    /// <summary>
    /// 上方向への移動
    /// </summary>
    MoveUp,

    /// <summary>
    /// 下方向への移動
    /// </summary>
    MoveDown,

    /// <summary>
    /// 左方向への移動
    /// </summary>
    MoveLeft,

    /// <summary>
    /// 右方向への移動
    /// </summary>
    MoveRight,

    /// <summary>
    /// 決定ボタン
    /// </summary>
    Confirm,

    /// <summary>
    /// キャンセルボタン
    /// </summary>
    Cancel,
}