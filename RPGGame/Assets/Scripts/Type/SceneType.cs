
/// <summary>
/// シーン種別タイプ
/// enum名とBuildSettingsのシーン名を一致させること
/// </summary>
public enum SceneType
{
    /// <summary>
    /// ゲームタイトルシーン
    /// </summary>
    GameTitleScene,

    /// <summary>
    /// 拠点シーン
    /// </summary>
    BaseScene,

    /// <summary>
    /// ダンジョンシーン
    /// </summary>
    DungeonScene,

    /// <summary>
    /// 戦闘シーン
    /// </summary>
    BattleScene,

    /// <summary>
    /// 戦闘結果シーン
    /// </summary>
    BattleResultScene
}